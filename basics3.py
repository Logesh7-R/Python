#Escape Sequence

a = 'it\'s sunny'   #single Quotes
b = "its \"kind of\" sunny"   #Double Quotes
c = "\t hi !!\nThankYou!!!.."  #Newline and Tab
d = "Hello \r Hi There!!.."   #Carriage return
e = "Hello\b madam!!.."  #Backspace
octl = '\110\123\123\111' #Octal number for character
hexl = '\x4e\x6f'  #hexagon number for character
print(a,b,c,d,e,octl,hexl)